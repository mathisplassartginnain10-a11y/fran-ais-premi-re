export function expandPassagesForWork(work) {
  const out = [];
  const key = work.oeuvre;

  if (BAC_THEATRE_SCENES[key]) {
    BAC_THEATRE_SCENES[key].forEach(([titre, ctx], i) => {
      out.push({ titre, contexte: ctx, prob: work.prob - i });
    });
  }
  if (BAC_ROMAN_PASSAGES[key]) {
    BAC_ROMAN_PASSAGES[key].forEach(([titre, ctx], i) => {
      out.push({ titre, contexte: ctx, prob: work.prob - i * 2 });
    });
  }
  if (BAC_POEMES[key]) {
    BAC_POEMES[key].forEach((titre, i) => {
      out.push({
        titre,
        contexte: `${work.ctx} — poème « ${titre} ».`,
        prob: work.prob - i * 2,
      });
    });
  }

  const genre = work.genre || '';
  const namedCount = out.length;
  if (/théâtre|theatre|trag|comed|drame/i.test(genre)) {
    const maxAct = namedCount >= 8 ? 3 : 5;
    const maxSc = namedCount >= 8 ? 6 : 10;
    for (let a = 1; a <= maxAct; a++) {
      for (let sc = 1; sc <= maxSc; sc++) {
        const titre = `Acte ${a}, scène ${sc}`;
        if (out.some(p => p.titre === titre)) continue;
        out.push({
          titre,
          contexte: `${work.ctx} — ${titre}.`,
          prob: Math.max(35, work.prob - 15 - a * 2 - sc),
        });
      }
    }
  } else if (/roman|nouvel|conte/i.test(genre)) {
    const scenes = ['Ouverture / incipit', 'Portrait du personnage', 'Scène clé', 'Monologue intérieur', 'Description', 'Dialogue', 'Climax', 'Dénouement', 'Épilogue'];
    scenes.forEach((titre, i) => {
      if (out.some(p => p.titre === titre)) return;
      out.push({ titre, contexte: `${work.ctx} — ${titre}.`, prob: Math.max(40, work.prob - 8 - i * 3) });
    });
    const maxCh = namedCount >= 6 ? 20 : 40;
    for (let ch = 1; ch <= maxCh; ch++) {
      out.push({
        titre: `Chapitre ${ch}`,
        contexte: `${work.ctx} — chapitre ${ch}.`,
        prob: Math.max(30, work.prob - 20 - ch),
      });
    }
  } else if (/poés|poeme|fable|ode/i.test(genre)) {
    const maxPo = namedCount >= 5 ? 15 : 30;
    for (let n = 1; n <= maxPo; n++) {
      out.push({
        titre: `Poème ${n}`,
        contexte: `${work.ctx} — poème n°${n}.`,
        prob: Math.max(32, work.prob - 12 - n),
      });
    }
  } else {
    for (let n = 1; n <= 20; n++) {
      out.push({
        titre: `Passage ${n}`,
        contexte: `${work.ctx} — extrait ${n}.`,
        prob: Math.max(30, work.prob - 10 - n),
      });
    }
  }

  return out;
}
