let CORPUS_DOC = [];

function buildCorpusDoc() {
  const doc = [];
  if (typeof buildGeneralProcedesHtml === 'function' && typeof PROC_DATA !== 'undefined' && PROC_DATA.length) {
    const procSearch = PROC_DATA.map(p => [p.name, p.def, p.ex, p.effet, p.cat, p.sub].join(' ')).join(' ');
    doc.push({
      id: 'GENERAL_PROCEDES',
      type: 'fiche',
      mat: 'proc',
      cat: 'Fiche générale',
      titre: 'Tous les procédés — fiche récapitulative',
      phrase: PROC_DATA.length + ' procédés · définition · exemple · effet · Comment ça marche · IPCI',
      reponse: 'GENERAL_PROCEDES',
      analyse: buildGeneralProcedesHtml(),
      search: 'GENERAL_PROCEDES procédés fiche imprimable récapitulatif ' + procSearch,
    });
  }
  if (typeof PROC_DATA !== 'undefined') {
    PROC_DATA.forEach((p, i) => {
      doc.push({
        id: 'PR-' + String(i + 1).padStart(3, '0'),
        type: 'procede',
        mat: 'proc',
        cat: p.cat,
        titre: p.name,
        sousCat: p.sub,
        phrase: p.ex,
        reponse: p.name,
        analyse: '<p><strong>Définition :</strong> ' + p.def + '</p><p><strong>Effet :</strong> ' + p.effet + '</p>',
        search: [p.name, p.def, p.ex, p.effet, p.cat, p.sub].join(' '),
      });
    });
  }
  if (typeof GRAM_COURS !== 'undefined') {
    GRAM_COURS.forEach((g, i) => {
      doc.push({
        id: 'GR-' + String(i + 1).padStart(3, '0'),
        type: 'grammaire',
        mat: 'gram',
        cat: g.cat,
        titre: g.name,
        phrase: g.def,
        reponse: g.name,
        analyse: g.content,
        search: [g.name, g.def, g.cat, g.cours].join(' '),
      });
    });
  }
  if (typeof EXERCICES !== 'undefined') {
    EXERCICES.forEach(ex => {
      if (typeof window !== 'undefined' && typeof window.bacExoAligned === 'function' && !window.bacExoAligned(ex)) return;
      doc.push({
        id: 'EX-' + ex.id,
        type: 'exercice',
        mat: 'proc',
        cat: ex.cat,
        titre: ex.q,
        auteur: ex.oeuvre,
        phrase: ex.text.replace(/\n/g, ' '),
        reponse: ex.opts[ex.ans],
        analyse: '<p>' + ex.expl + '</p>',
        search: [ex.q, ex.text, ex.expl, ex.oeuvre, ex.cat].join(' '),
      });
    });
  }
  CORPUS_PHRASES.forEach(p => {
    if (typeof window !== 'undefined' && typeof window.bacCorpusPhraseAligned === 'function' && !window.bacCorpusPhraseAligned(p)) return;
    doc.push({
      id: p.id,
      type: 'phrase',
      mat: p.mat,
      cat: p.cat,
      titre: p.procede || p.reponse,
      auteur: p.auteur,
      phrase: p.phrase,
      reponse: p.reponse,
      procede: p.procede,
      effet: p.effet,
      analyse: '<p>' + p.analyse + '</p>' + (p.effet ? '<p><strong>Effet :</strong> ' + p.effet + '</p>' : ''),
      search: [p.phrase, p.analyse, p.reponse, p.procede, p.auteur, p.cat].join(' '),
    });
  });
  CORPUS_METHOD.forEach(m => {
    doc.push({
      id: m.id,
      type: 'methode',
      mat: 'both',
      cat: m.cat,
      titre: m.title,
      phrase: '',
      reponse: m.title,
      analyse: m.body,
      search: m.title + ' ' + m.cat,
    });
  });
  if (typeof AUTEURS_DATA !== 'undefined') {
    AUTEURS_DATA.forEach(a => {
      const parts = [];
      if (a.mouvement) parts.push('<p><strong>Mouvement :</strong> ' + a.mouvement + '</p>');
      if (a.oeuvres) parts.push('<p><strong>Œuvres principales :</strong> ' + a.oeuvres + '</p>');
      if (a.intro?.length) parts.push('<p><strong>Pour l\'introduction :</strong></p><ul>' + a.intro.map(i => '<li>' + i + '</li>').join('') + '</ul>');
      if (a.dev) parts.push('<p><strong>Développement :</strong> ' + a.dev + '</p>');
      if (a.note) parts.push('<p><em>' + a.note + '</em></p>');
      doc.push({
        id: 'AU-' + a.id.toUpperCase(),
        type: 'auteur',
        mat: 'proc',
        cat: 'Principaux auteurs',
        titre: a.nom,
        auteur: [a.genre, a.dates].filter(Boolean).join(' · '),
        phrase: a.oeuvres || '',
        reponse: a.mouvement || a.genre || a.nom,
        analyse: (parts.length ? parts.join('') : '<p>Fiche auteur — voir genre et dates ci-dessus.</p>')
          + '<p class="doc-source"><em>Source : docs/Principaux auteurs.docx</em></p>',
        search: [a.nom, a.genre, a.dates, a.mouvement, a.oeuvres, a.note, a.dev, ...(a.intro || [])].join(' '),
      });
    });
  }
  CORPUS_DOC = doc;
  return doc;
}

buildCorpusDoc();
